function cliqueOfertas(fn) {
    this.router.navigate[this.url];
    this.router.events.subscribe((event) => {
        if (event instanceof NavigationEvent) {
            const url = event.url;
            if (url === this.url) {
                fn;
            }
        }
    });
}

function cliqueOfertasVoltar() {
    this.cliqueOfertas(this.analytics());
}

